	private Image cellRender(int idx) {
		Image floor = new ImageIcon(App.class.getResource("/assets/images/map/floor2.png")).getImage();
		Image wall = new ImageIcon(App.class.getResource("/assets/images/map/wall.png")).getImage();
		
		if(idx == 1) {
			return wall;
		}
		else {
			return floor;
		}
	}
	
	public void render(Graphics2D g2) {
		int cw = 28, ch = cw; // cell dimensions
		int cx = 0, cy = 0; // current starting x and y position
		
		for(int i = 0; i < map.length; i++) {
			for(int j = 0; j < map[i].length; j++) {
				g2.drawImage(cellRender(map[i][j]), cx, cy, cw, ch, null);
				cx += cw;
			}
			cy += ch; cx = 0;
		}
	}

